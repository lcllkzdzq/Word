package com.vulpes.word.Model;

public class ResponseHeaderModel {
    private int status;
    private String desc;

    public ResponseHeaderModel(int status) {
        this.status = status;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
